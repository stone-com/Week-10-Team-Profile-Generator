//function to create cards for each user submitted employee, then push to an array
// push array to page
const createHTML = (data) => {
  // initialize empty array, will be filled with all employees from user submitted data
  let employeeArray = [];
  //loop through all employees in data from inquirer, create a roll variable for each employee
  for (let i = 0; i < data.length; i++) {
    const employee = data[i];
    const role = employee.getRole();

    // if role is manager, call createManager function
    if (role === 'Manager') {
      const managerCard = createManager(employee);
      //push new card to employeeArray
      employeeArray.push(managerCard);
    }

    // if role is engineer, call createEngineer function
    if (role === 'Engineer') {
      const engineerCard = createEngineer(employee);
      //push new card to employeeArray
      employeeArray.push(engineerCard);
    }

    // if role is intern, call createIntern function
    if (role === 'Intern') {
      const internCard = createIntern(employee);
      //push new card to employeeArray
      employeeArray.push(internCard);
    }
  }

  //turn array into a string with .join method. (needs to be a string since its all HTML)
  const cards = employeeArray.join('');

  //call createTeamPage function passing in string of generated cards, save to variable and return it.
  const generateTeam = createTeamPage(cards);
  return generateTeam;
};

